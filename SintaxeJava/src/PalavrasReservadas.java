public class PalavrasReservadas {
    /*
     * Controles de pacote
     * 
     * import - importa pacotes ou classes para dentro do projeto
     * package - especifica o pacote que todas as classes de um arquivo pertencem
     */

    /*
     * Modificadores de acesso
     * 
     * public - acesso a qualquer classe
     * private - acesso apenas dentro da classe
     * protected - acesso por classes no mesmo pacote e subclasses
     */

    /*
     * Primitivos
     * 
     * boolean
     * byte
     * char
     * double
     * float
     * int
     * long
     * short
     * void - indica que o metodo não tem retorno
     */

    /*
     * Modificadores de classes, variáveis ou metódos
     * 
     * asbtract - classe que não pode ser instaciada ou metodo que precisa ser
     * implementado por uma subclasse não abstrata
     * class - especifica uma classe
     * extends - indica a superclasse que a subclasse está estendendo
     * final - impossibilita que uma classe seja estendida, que um metodo seja
     * sobrescrito ou que uma variavel seja reinicializada
     * implements - indica as interfaxes que uma classe irá implementar
     * interface - especifica uma interface
     * native - indica que o método está escrito em uma linguagem dependente da
     * plataforma, como o C
     * new - instancia um novo objeto, chamando seu construtor
     * static - faz um metodo ou uam variavel pertencer à classe ao invés de às
     * instacias
     * strictfp - usado em frente a um método ou classe para indicar que os números
     * de ponto flutuante seguiraõ as regras de ponto flutuante em todas as
     * expressoes
     * synchronized - uindica que um metodo só pode ser acessado por uma thread de
     * cada vez
     * transient - impede a seriaalização de campos
     * volatile - indica que uma variavel pode ser alterada durante o uso de threads
     */

    /*
     * Controle de fluxo dentro de um bloco de código
     * 
     * break - sai do bloco de codigo em que ele esta
     * case - executa um bloco de código dependendo do teste do switch
     * continue - pula a execução do código que viria após essa linha e vai para a
     * proxima passagem do loop
     * default- executa esse bloco de codigo caso nenhum dos teste de switch-case
     * seja verdadeiro
     * do - executa um bloco de código uma vez, e então realiza um teste em conjunto
     * com o while para determinar se o bloco deverá ser executado novamente
     * else - executa um bloco de código alternativo caso o teste if seja falso
     * for - usado para realizar um loop condicional de um bloco de código
     * if - usado para realizar um teste lógico de verdadeiro o falso
     * instanceof - determina se um objeto é uma instância de determinada classe,
     * superclasse ou interface
     * return - retorna de um método sem executar qualquer código que venha depois
     * desta linha (também pode retornar uma variável)
     * switch - indica a variável a ser comparada nas expressões case
     * while - executa um bloco de código repetidamente enquanto a condição for
     * verdadeira
     */

    /*
     * Tratamento de erros
     * 
     * assert - testa uma expressão condicional para verificar uma suposição do
     * programador
     * catch - declara o bloco de código usado para tratar uma exceção
     * finally - bloco de código, após um try-catch, que é executado
     * independentemente do fluxo de programa seguido ao lidar com uma exceção
     * throw - usado para passar uma exceção para o método que o chamou
     * throws - indica que um método pode passar uma exceção para o método que o
     * chamou
     * try - bloco de código que tentará ser executado, mas que pode causar uma
     * exceção
     */

    /*
     * Variáveis de referência
     * 
     * super - refere-se a superclasse imediata
     * this - refere-se a instância atual do objeto
     */
}
